					<ul><h3 style="color:#4169E1;"><u>CAT 2019 Selection Procedure</u></h3></ul>

					<p>All the participating institutes have their own shortlisting criteria for admission. The basic norm is to clear the sectional and overall CAT 2019 Cut Off of the respective institutes. Applicants who fulfill such criteria will move forward to the next stage of selection process.</p></br>

					<p>CAT Selection Procedure followed by IIMs consists of Group Discussion (GD), Written Ability Test (WAT) and Personal Interview (PI) rounds. These are explained briefly as follows-</p></br>


					<ol>
					<li>Candidates shortlisted based on their CAT scores have to appear for GD, WAT and PI rounds held by the IIMs or other b-schools.</li>

					<li>In the WAT round, students have to write an essay on the topic provided by the institute.</li>

					<li>The topic can be related to socio-political issues, current affairs or some idea. The essay should generally be completed within 30 minutes.</li>

					<li>For the GD round, applicants are divided into groups of 8-10 members wherein, they are required to discuss about the given topic.</li>

					<li>PI is the final round wherein aspirants are interviewed by a panel of 4-8 judges. This panel assesses the profile of individuals to see if he is fit for the chosen course.</li>

					<p>Many top business schools follow a similar selection process. After all the rounds are conducted the institutes publish the final merit list. This list is created after considering all rounds of selection and CAT 2019 scores. Candidates are offered admission to various courses based on this merit list</p></br>
				
