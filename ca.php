						<p class="m_1">
						 CA Foundation exam is an entry-level exam for students interested to pursue a career in Chartered Accountancy. CA Foundation Exam is conducted by the Institute of Chartered Accountants of India (ICAI) twice a year. It is a national level exam which consists of four papers. CA Foundation exam was previously called as CA – CPT exam.</p><br>

						 <p class="m_1">
						 Students need to clear three major stages to become a Chartered Accountant (CA). The three stages are Foundation, Intermediate, and Final stage. Candidates who have either passed Class 12 or going to appear for Class 12  exam conducted by an examining body constituted by the law in India (or any examination recognized by the Central Government as equivalent thereto) are eligible for this exam. ICAI will conduct the upcoming CA Foundation exam in May 2019. All eligible candidates need to register with Board Of Studies (BOS) and apply for the Exam. This article will provide you with complete details regarding CA Foundation 2019.</p><br>

						

						

						

