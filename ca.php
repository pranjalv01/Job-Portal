					<ul><h3 style="color:#4169E1;"><u>Exam Pattern</u></h3></ul>
					<p>CA Foundation exam consists of 4 papers. Paper 1 and Paper 2 consists of subjective type questions. Paper 3 and Paper 4 consists of objective type questions.</p></br>

					<ol type="1">
					<li>Each paper carries 100 marks and the maximum duration is 3 hours.</li>
				
					<li>Paper 3 and paper 4 have the negative marking scheme and 1/4th of the marks will be deducted for every wrong answer.</li>

					<li>Candidates must score a minimum of <b> 40% marks in each paper </b> and a minimum of  <b>50% marks in aggregate </b> of all the papers to pass the CA Foundation exam.</li>
					</ol>

					
					<p>The papers of the exam are listed below:</p></br>

					<p><b>Paper 1: </b>Principles And Practice Of Accounting (100 Marks)</p></br>

					<p><b>Paper 2: </b>Business Laws And Business Correspondence</p>

					<ol type="1">
					<li>Business Laws (60 Marks)</li>
				
					<li>Business Correspondence And Reporting (40 Marks)</li>
					
					</ol><br>

					<p><b>Paper 3: </b>Business Mathematics, Logical Reasoning And Statistics </p>

					<ol type="a">
					<li> Business Mathematics (40 Marks)</li>
				
					<li>Logical Reasoning(20 Marks)</li>

					<li> Statistics (40 Marks)</li>
					</ol><br>

					<p><b>Paper 4: </b>Business Economics And Business & Commercial Knowledge</p>

					<ol type="a">
					<li>Business Economics (60 Marks)</li>
				
					<li>Business and Commercial Knowledge (40 Marks)</li>

					</ol><br>
				
					

