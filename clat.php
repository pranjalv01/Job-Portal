					<ul><h3 style="color:#4169E1;"><u>CLAT Eligibility 2019</u></h3></ul>
					<p>CLAT Core Committee defines the eligibility for UG and PG Programs. The eligibility is measured on the following grounds</p></br>
					
					<ul><h4 style="color:#4129f1;"><u>FOR UG PROGRAM</u></h4></ul>

					<ol type="a">
					<li><b>Age Limit:</b> As per revised rules, there is no upper age limit for CLAT 2019.</li><br>

					<li><b>Academic Qualification:</b> Candidate must have passed 10+2  or any equivalent examination.</li><br>

					<li><b>Appearing Candidates:</b> Candidates appearing in the qualifying examination in April/ May 2019 are also eligible to appear in CLAT exam.  </li><br>
									
					</ol>

					<ul><h4 style="color:#4129f1;"><u>FOR PG PROGRAM</u></h4></ul>

						<ol type="a">
					<li><b>Age Limit:</b> There is no upper age limit for the post-graduate program.</li><br>

					<li><b>Academic Qualification:</b> Candidate must have an LLB or any other equivalent degree.</li><br>

					<li><b>Appearing Candidates:</b> Candidates appearing in the qualifying examination in March/April 2019 are also eligible to appear in exam. However, they have to show a hard copy of the proof of their passing the qualifying examination in the respective National Law Universities at the time of admission.  </li><br>
									
					</ol>

					<br><br>

					<ul><h3 style="color:#4169E1;"><u>CLAT Exam Pattern</u></h3></ul>

					<p>The national law entrance exam will be of two hours duration. The offline mode exam will have 200 objective type questions. Highlights of exam pattern of CLAT is as follows:</p></br>

					<ol  type="a"> 
					<li><b>Mode of Examination:</b> Offline</li>

					<li><b>Number of Sections:</b> In LLB- 5 Sections and In LLM – 3 sections</li>

					<li><b>Nature of Questions:</b> Objective type (MCQs)</li>

					<li><b>Total Questions:</b> 200 Questions in LLB and 150 Questions in LLM</li>

					<li><b>Marking Scheme:</b> 1 mark of each question</li>

					<li><b>Negative Marking:</b> 0.25 marks deducted for a wrong answer</li>

					</ol>
					
