					<ul><h3 style="color:#4169E1;"><u>Age Limit & Educational Qualifications for IAS</u></h3></ul>
					<p>Candidates have to fulfill the eligibility criteria set by the authorities to take the exam. The eligibility parameters are set on the basis of Nationality, Age Limit, Educational Qualifications, Number of Attempts and Physical Standards</p></br>
					
					<ul><h3 style="color:#4169E1;"><u>Nationality</u></h3></ul>

					<ol type="a">
					<li>For IAS and IPS, applicants must be a citizen of India.</li>
				
					<li>Other services require candidates to be either,</li>
					</ol>
				<ol>
					<li>Citizen of India</li>

					<li>Subject of Nepal</li>

					<li>Subject of Bhutan</li>

					<li>Tibetan refugee who came to India before January 1, 1962 to settle permanently.</li>

					<li>An Indian origin migrant from Pakistan, Burma, Sri Lanka, East African countries of Kenya, Uganda, the United Republic of Tanzania, Zambia, Malawi, Zaire, Ethiopia and Vietnam who came to permanently settle in India.</li>
				</ol>
				<br>
				<br>

