					 <ul><h3 style="color:#4169E1;"><u>UPSC IAS 2019 Syllabus</u></h3></ul>
					<p>IAS Syllabus is decided by Union Public Service Commission (UPSC) each year. Each stage in IAS exam has a different syllabus.</br>

					<ol>
					<li>IAS Syllabus for Prelims evaluates a candidate’s knowledge and aptitude in current affairs.</li>

					<li>IAS Syllabus for Mains assesses a candidate’s academic expertise in his specialized subject. It also tests his ability to present knowledge in a clear and coherent manner and his overall intellectual traits and understanding of concepts.</li>
					<li>The prelims section of IAS Exam 2019 covers questions from History, Polity, Geography and Agriculture, Science and Technology, Ecology and Environment, Economics, Arts and Culture, Social Welfare and GK. </li>

					<li>The scope of IAS Syllabus for Mains section as optional subject papers (Paper VI and Paper VII) for the examination is broad of the honors degree level.</li>

					<li>In the case of Engineering, Medical Science and law, the level corresponds to the bachelors’ degree.</li>

				</ol>
