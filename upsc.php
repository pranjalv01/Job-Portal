						<p class="m_1">
						IAS Exam, also known as Indian Administrative Service, is a national level competition exam conducted by UPSC annually to recruit candidates for civil services like IPS, IFS (Foreign), IAAS, IRS, IDAS, CAPF-AF and others. Nearly 2 lac candidates appear for the Civil Services Examinations each year.</p><br>

						<ol>
